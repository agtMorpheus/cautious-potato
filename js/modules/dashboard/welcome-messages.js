/**
 * Get the message of the day based on current date
 * Uses day of year to cycle through messages
 * @returns {string} The message for today
 */
export function getMessageOfDay() {
    const today = new Date();
    const startOfYear = new Date(today.getFullYear(), 0, 0);
    const diff = today - startOfYear;
    const oneDay = 1000 * 60 * 60 * 24;
    const dayOfYear = Math.floor(diff / oneDay);
    return welcomeMessages[dayOfYear % welcomeMessages.length];
}

/**
 * Get the message index for today (1-500)
 * @returns {number} The message number for today (1-based)
 */
export function getMessageIndex() {
    const today = new Date();
    const startOfYear = new Date(today.getFullYear(), 0, 0);
    const diff = today - startOfYear;
    const oneDay = 1000 * 60 * 60 * 24;
    const dayOfYear = Math.floor(diff / oneDay);
    return (dayOfYear % welcomeMessages.length) + 1;
}

/**
 * Get a random message from the collection
 * @returns {string} A random welcome message
 */
export function getRandomMessage() {
    const randomIndex = Math.floor(Math.random() * welcomeMessages.length);
    return welcomeMessages[randomIndex];
}

/**
 * Get a message by specific index (1-based)
 * @param {number} index - The message index (1-500)
 * @returns {string} The message at that index
 */
export function getMessageByIndex(index) {
    const arrayIndex = Math.max(0, Math.min(index - 1, welcomeMessages.length - 1));
    return welcomeMessages[arrayIndex];
}

/**
 * Get total number of messages
 * @returns {number} Total count of messages
 */
export function getTotalMessages() {
    return welcomeMessages.length;
}

